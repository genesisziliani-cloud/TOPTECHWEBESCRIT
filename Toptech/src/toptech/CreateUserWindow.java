    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nombre = txtnombre1.getText().trim();
        String apellido = txtapellido1.getText().trim();
        String dni = txtdni1.getText().trim();
        String usuario = txtusuario1.getText().trim();
        String pass = new String(jPassword.getPassword());
        String rol = (String) roles.getSelectedItem();

        if (nombre.isEmpty() || apellido.isEmpty() || dni.length()!=8 || usuario.isEmpty() || pass.length()!=6 || roles.getSelectedIndex()==0) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios, rol debe seleccionarse y DNI/contraseña con longitud correcta.");
            return;
        }
        if (userManager.findUserByUsuario(usuario) != null) {
            JOptionPane.showMessageDialog(this, "El usuario generado ya existe, modifica el nombre o apellido.");
            return;
        }
        if (userManager.findUserByDNI(dni) != null) {
            JOptionPane.showMessageDialog(this, "El DNI ya existe en el sistema.");
            return;
        }
        // Ajuste de rol según tus opciones
    // ✅ CORREGIDO: Ajuste de rol incluyendo CLIENTE
String rolFinal;
if (rol.equalsIgnoreCase("ADMINISTRADOR")) {
    rolFinal = "ADMIN";
} else if (rol.equalsIgnoreCase("TECNICO")) {
    rolFinal = "TECNICO";
} else if (rol.equalsIgnoreCase("CLIENTE")) {
    rolFinal = "CLIENTE";
} else {
    rolFinal = "CLIENTE"; // Por defecto si no se selecciona nada
}

User nuevo = new User(dni, nombre, apellido, usuario, pass, rolFinal);
userManager.addUser(nuevo);
JOptionPane.showMessageDialog(this, "Usuario registrado correctamente.\nRol: " + rolFinal);
if (parentWindow != null) parentWindow.setVisible(true);
dispose(); 
    }//GEN-LAST:event_btnGuardarActionPerformed

