    // Lógica de autollenado y validaciones
    private void inicializarEventos() {
        // Autogenerar usuario mientras se escriben nombre y apellido
        KeyAdapter nombreApellidoListener = new KeyAdapter() {
            public void keyReleased(KeyEvent e) {
                String nombre = txtnombre1.getText().replaceAll("[^a-zA-Z]", "");
                String apellido = txtapellido1.getText().replaceAll("[^a-zA-Z]", "");
                txtnombre1.setText(nombre);
                txtapellido1.setText(apellido);

                if (nombre.length() >= 3 && apellido.length() >= 3) {
                    String usuarioBase = nombre.substring(0, 3).toLowerCase() + apellido.substring(0, 3).toLowerCase();
                    String usuarioFinal = usuarioBase;
                    int intentos = 0;
                    while (userManager.findUserByUsuario(usuarioFinal) != null && intentos < 10) {
                        usuarioFinal = nombre.substring(0, 3).toLowerCase() + randomizeApellido(apellido);
                        intentos++;
                    }
                    txtusuario1.setText(usuarioFinal);
                } else {
                    txtusuario1.setText("");
                }
            }
        };
        txtnombre1.addKeyListener(nombreApellidoListener);
        txtapellido1.addKeyListener(nombreApellidoListener);

        // Limitar a 8 dígitos el DNI
        txtdni1.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e) {
                char c = e.getKeyChar();
                if (!Character.isDigit(c) || txtdni1.getText().length() >= 8) {
                    e.consume();
                }
            }
        });

        // Mostrar/ocultar contraseña
        btnmostrar.addActionListener(e -> {
            if (jPassword.getEchoChar() == (char)0) {
                jPassword.setEchoChar('*');
            } else {
                jPassword.setEchoChar((char)0);
            }
        });
    }

    private String randomizeApellido(String apellido) {
        Random rand = new Random();
        char[] letras = apellido.toLowerCase().toCharArray();
        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < 3; i++) {
            sb.append(letras[rand.nextInt(letras.length)]);
        }
        return sb.toString();
    }

